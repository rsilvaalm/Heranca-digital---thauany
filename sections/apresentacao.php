          <p>Além disso, considerando que, no direito sucessório brasileiro, aplica-se a legislação vigente ao tempo da morte, a manifestação prévia de vontade do titular assume especial relevância. Mesmo que o ordenamento venha a sofrer alterações futuras, o testamento continua sendo uma das formas mais seguras de orientar a sucessão dos bens digitais, proteger a privacidade do falecido e preservar sua honra, imagem e memória.</p>
