            <p>Desse modo, quaisquer dessas obras, quando produzidas, fixadas ou armazenadas em meio digital, podem ser objeto de proteção autoral. Entretanto, embora o Brasil possua legislação apta a proteger essas criações, a disciplina legal ainda se mostra genérica diante das particularidades do ambiente virtual. A velocidade da circulação de informações, a facilidade de reprodução de conteúdo e a multiplicação de plataformas digitais tornam mais complexa a tutela dos direitos autorais nesse espaço.</p>
