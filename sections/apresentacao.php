          <p>É justamente nesse ponto que surgem os maiores desafios. Se, por um lado, os bens digitais podem integrar a herança, por outro, muitos deles possuem conteúdo profundamente íntimo, como mensagens privadas, fotografias pessoais, vídeos, áudios, conversas e arquivos armazenados em contas individuais. Nesses casos, discute-se se a transmissão irrestrita desses conteúdos aos herdeiros poderia violar direitos da personalidade do falecido, como a imagem, a intimidade, a privacidade e a honra.</p>
